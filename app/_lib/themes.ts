export function applyTheme(theme: ThemeColor, isDark = false): void {
    const themeData = isDark ? darkThemeColors[theme] : themeColors[theme];

    Object.entries(themeData.variables).forEach(([key, value]) => {
        document.documentElement.style.setProperty(key, value);
    });
}
